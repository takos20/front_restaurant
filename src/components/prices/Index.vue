      let self = this;
      self.loading.list = true;
      let params = {};
      if (self.filter.name !== null) {
        params["dish_name"] = self.filter.name;
      }
      self._search = params;
      //console.log("FILTRE", this._search);
      self
        .getItems()
        .then(() => {
          self.$refs.filterForm.closeDialog();
        })
        .finally(() => {
          self.$refs.filterForm.stopLoadingBtn();
        });
    },
